import numpy as np
x = np.array([  [0,1,2],            #Initializing MATRIX-1
                [3,4,5],
                [6,7,8]])
y = np.array([  [-4,-3,-2],         #Initializing MATRIX-2
                [ 0, 2, 1],
                [ 1, 4, 2]])
print("Sum of matrices:\n",np.add(x,y)) #Printing Result